public class HeartShape {
    public static void main(String[] args) {
        String[] heart = {
            "  **     **  ",
            " ****   **** ",
            "****** ******",
            " *********** ",
            "  *********  ",
            "   *******   ",
            "    *****    ",
            "     ***     ",
            "      *      "
        };

        for (String line : heart) {
            System.out.println(line);
        }
    }
}
